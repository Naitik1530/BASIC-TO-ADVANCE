//program to display alphabet a to z using loop
public class AlphabetDisplay {
  public static void main(String[] args) {
    char c;
    for(c='A';c<='Z';++c){
      System.out.print(c + " ");
    }
    //lowercase alphabet
    System.out.println();
    for(c='a';c<='z';++c){
      System.out.print(c + " ");
    
  }
  
}
}
